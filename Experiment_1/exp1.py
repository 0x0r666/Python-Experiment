
#Aim: Demonstrate Various Data Types and Operators in Python.


print("------ DATA TYPE ------")

a = 10          
b = 3.5              

print("Integer:", a)
print("Float:", b)

print("\n------ ARITHMETIC OPERATORS ------")

x = 10
y = 5

print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)
print("Modulus:", x % y)

print("\n------ RELATIONAL OPERATORS ------")

print("x > y :", x > y)
print("x < y :", x < y)
print("x == y :", x == y)
print("x != y :", x != y)

print("\n------ ASSIGNMENT OPERATORS ------")

n = 10
print("Initial value:", n)

n += 5
print("After += :", n)

n -= 3
print("After -= :", n)

n *= 2
print("After *= :", n)

print("\n------ BITWISE OPERATORS ------")

a = 6
b = 3

print("a & b :", a & b)
print("a | b :", a | b)
print("a ^ b :", a ^ b)
print("a << 1 :", a << 1)
print("a >> 1 :", a >> 1)